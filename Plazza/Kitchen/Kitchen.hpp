{

///////////////////////////////////////////////////////////////////////////////
/// \brief
///
///////////////////////////////////////////////////////////////////////////////
class Kitchen
{
public:
    ///////////////////////////////////////////////////////////////////////////
    ///
    ///////////////////////////////////////////////////////////////////////////
    std::vector<Plazza::Cook> cooks;        //<!
    Plazza::Stock stock;                    //<!
    float multiplier;                       //<!

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param cook_nb
    ///
    ///////////////////////////////////////////////////////////////////////////
    Kitchen(int cook_nb = 1, float multiplier = 1.0f,
        std::chrono::milliseconds restock_time)
        : cooks(cook_nb)
        , stock()
        , multiplier(multiplier)
    {
        stock.restock_time = restock_time;
    }
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    ///////////////////////////////////////////////////////////////////////////
    ~Kitchen();
};

} // !namespace Plazza